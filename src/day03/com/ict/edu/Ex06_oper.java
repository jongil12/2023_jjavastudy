package day03.com.ict.edu;

public class Ex06_oper {
public static void main(String[] args) {
	//대입연산 : = , += , -= , *= , /= , %/
	// A = B  => 변수 A에 데이터 B를 저장
	// A += B => 변수 A가 가지고 있는 데이터와 B 가 가지고 있는 데이터를 더한후 A 에저장한다.
	
	int su1 = 7 ;
	su1 += 4 ; //su1 = su1 + 4
	System.out.println("결과 : " + su1);
	
	su1 -= 4 ; //su1 = su1 - 4
	System.out.println("결과 : " + su1);
	
	}
}
